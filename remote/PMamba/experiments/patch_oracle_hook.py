"""Add oracle-and-fusion reporting to main.py's eval + telegram hook.

Loads cached PMamba test predictions once, accumulates current model's softmax
probs across eval, then computes oracle accuracy and best-alpha fusion at the
end of each eval pass. Both are printed and appended to the telegram message.

Cache file: work_dir/pmamba_branch/pmamba_test_preds.npz (probs, labels).
Generated by precompute_pmamba_preds.py.

Also adds:
- self._eval_probs / self._eval_labels accumulators (cleared per eval pass)
- _maybe_compute_oracle(epoch, mode) method
"""
from pathlib import Path

PATH = Path("main.py")
src = PATH.read_text(encoding="utf-8")

# 1. __init__ hook: load the cache (no-op if absent).
INIT_OLD = '        self.telegram_bot_token = "8049556095:AAH0c0KB0DmzFtcW0s97ZS_kQ8ux9gX72eE"\n        self.telegram_chat_id = None'
INIT_NEW = INIT_OLD + '''
        # Oracle/fusion telemetry vs PMamba @ e110 (cached probs).
        self._pmamba_probs = None
        self._pmamba_labels = None
        self._eval_probs = []
        self._eval_labels = []
        self._latest_oracle = None
        self._latest_fusion = None
        self._latest_fusion_alpha = None
        try:
            _cache = "work_dir/pmamba_branch/pmamba_test_preds.npz"
            import numpy as _np, os as _os
            if _os.path.exists(_cache):
                _d = _np.load(_cache)
                self._pmamba_probs = _d["probs"]
                self._pmamba_labels = _d["labels"]
        except Exception:
            pass'''
assert INIT_OLD in src, "init anchor missing"
src = src.replace(INIT_OLD, INIT_NEW, 1)

# 2. eval(): clear accumulators at start of each pass; append softmax probs
EVAL_OLD = '''    def eval(self, loader_name):
        self.model.eval()
        for l_name in loader_name:
            loader = self.data_loader[l_name]
            loss_mean = []
            for batch_idx, data in enumerate(loader):'''
EVAL_NEW = '''    def eval(self, loader_name):
        self.model.eval()
        self._eval_probs = []
        self._eval_labels = []
        for l_name in loader_name:
            loader = self.data_loader[l_name]
            loss_mean = []
            for batch_idx, data in enumerate(loader):'''
assert EVAL_OLD in src, "eval anchor missing"
src = src.replace(EVAL_OLD, EVAL_NEW, 1)

# 3. eval() accumulate probs after output is computed
ACC_OLD = '''                loss_mean += self.loss(output, label).cpu().detach().numpy().tolist()
                self.stat.update_accuracy(output.data.cpu(), label.cpu(), topk=self.topk)'''
ACC_NEW = '''                loss_mean += self.loss(output, label).cpu().detach().numpy().tolist()
                self.stat.update_accuracy(output.data.cpu(), label.cpu(), topk=self.topk)
                self._eval_probs.append(torch.softmax(output.detach().float(), dim=1).cpu().numpy())
                self._eval_labels.append(label.detach().cpu().numpy())'''
assert ACC_OLD in src, "acc anchor missing"
src = src.replace(ACC_OLD, ACC_NEW, 1)

# 4. print_inf_log hook: compute oracle + fusion, extend telegram message
ORACLE_OLD = '''            # Format message as: Train: train acc train loss Test: test acc test loss
            if train_acc is not None and train_loss is not None:'''
ORACLE_NEW = '''            self._maybe_compute_oracle(epoch, mode)
            # Format message as: Train: train acc train loss Test: test acc test loss
            if train_acc is not None and train_loss is not None:'''
assert ORACLE_OLD in src, "oracle hook anchor missing"
src = src.replace(ORACLE_OLD, ORACLE_NEW, 1)

# 5. Extend both message branches to include oracle/fusion lines. Use
# emoji-free anchors so we don't have to guess the exact bytes.
MSG_OLD = '''                message += f"Test: {prec1:.1f} {prec5:.1f}"
                if is_new_best:'''
MSG_NEW = '''                message += f"Test: {prec1:.1f} {prec5:.1f}"
                if self._latest_oracle is not None:
                    message += f"\\nOracle: {self._latest_oracle:.2f}% | Fusion a={self._latest_fusion_alpha:.2f} -> {self._latest_fusion:.2f}%"
                if is_new_best:'''
assert MSG_OLD in src, "msg anchor 1 missing"
src = src.replace(MSG_OLD, MSG_NEW, 1)

MSG2_OLD = '''                message += f"Test: {prec1:.1f} {prec5:.1f}\\n"
                if is_new_best:'''
MSG2_NEW = '''                message += f"Test: {prec1:.1f} {prec5:.1f}\\n"
                if self._latest_oracle is not None:
                    message += f"Oracle: {self._latest_oracle:.2f}% | Fusion a={self._latest_fusion_alpha:.2f} -> {self._latest_fusion:.2f}%\\n"
                if is_new_best:'''
assert MSG2_OLD in src, "msg anchor 2 missing"
src = src.replace(MSG2_OLD, MSG2_NEW, 1)

# 6. Add the helper method (inject before save_model)
HELPER_OLD = '''    def save_model(self, epoch, model, optimizer, save_path):'''
HELPER_NEW = '''    def _maybe_compute_oracle(self, epoch, mode):
        """Populate self._latest_oracle and self._latest_fusion (percentages)
        using cached PMamba test-set probs + this eval pass' accumulated probs.
        """
        self._latest_oracle = None
        self._latest_fusion = None
        self._latest_fusion_alpha = None
        if self._pmamba_probs is None or not self._eval_probs:
            return
        try:
            import numpy as np
            probs = np.concatenate(self._eval_probs, axis=0)                 # (N, 25)
            labels = np.concatenate(self._eval_labels, axis=0)               # (N,)
            if probs.shape[0] != self._pmamba_probs.shape[0]:
                self.recoder.print_log(
                    f"[oracle] skip: {probs.shape[0]} model preds vs {self._pmamba_probs.shape[0]} cached PMamba preds"
                )
                return
            # Labels should agree with cache; use cache labels as reference.
            ref_labels = self._pmamba_labels if self._pmamba_labels.shape[0] == probs.shape[0] else labels
            pm_correct = self._pmamba_probs.argmax(1) == ref_labels
            md_correct = probs.argmax(1) == ref_labels
            oracle = (pm_correct | md_correct).mean() * 100
            best_a, best_acc = 1.0, (pm_correct).mean() * 100
            for ai in range(0, 105, 5):
                a = ai / 100.0
                fp = (a * self._pmamba_probs + (1 - a) * probs).argmax(1)
                acc = (fp == ref_labels).mean() * 100
                if acc > best_acc:
                    best_acc = acc; best_a = a
            self._latest_oracle = float(oracle)
            self._latest_fusion = float(best_acc)
            self._latest_fusion_alpha = float(best_a)
            self.recoder.print_log(
                f"[oracle] epoch={epoch} mode={mode} oracle={oracle:.2f}% fusion[a={best_a:.2f}]={best_acc:.2f}%"
            )
        except Exception as e:
            self.recoder.print_log(f"[oracle] failed: {e}")

    def save_model(self, epoch, model, optimizer, save_path):'''
assert HELPER_OLD in src, "helper anchor missing"
src = src.replace(HELPER_OLD, HELPER_NEW, 1)

PATH.write_text(src, encoding="utf-8")
print("patched main.py: oracle + fusion in eval, printed and appended to telegram")
